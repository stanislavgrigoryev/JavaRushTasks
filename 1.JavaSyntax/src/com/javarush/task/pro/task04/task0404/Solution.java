package com.javarush.task.pro.task04.task0404;

/* 
Заполненный прямоугольник
*/

public class Solution {
    public static void main(String[] args) {
        int x = 0;
        while (x<5){
            int y = 1;
            while (y<10){
                System.out.println("Q");
                y++;
            }
            System.out.println();
            x++;
        }

    }
}